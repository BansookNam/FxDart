  "lib/src/dart_aliases.dart"
  "lib/src/util/timing.dart"
  "lib/src/util/shuffle.dart"
)

# Strips lines that start with `import `, `export `, or the exact `library;`
# directive.
strip_directives() {
  grep -vE '^(import |export )' "$1" | grep -vE '^library;$'
}

